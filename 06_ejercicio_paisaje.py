import turtle
t = turtle.Turtle()
t.color("skyblue")
t.fillcolor("skyblue")
t.begin_fill()
t.goto(-300,200)
t.goto(300,200)
t.goto(300, -100)
t.goto(-300, -100)
t.goto(-300, 200)
t.end_fill()
t.penup()
t.goto(-200,150)
t.color("yellow")
t.dot(100)
t.penup()
t.goto(-300,-100)
t.pendown()
t.color("green")
t.fillcolor("green")
t.begin_fill()
t.goto(300, -100)
t.goto(300, -300)
t.goto(-300, -300)
t.goto(-300, -100)
t.end_fill()
t.hideturtle()
turtle.done()